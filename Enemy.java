import greenfoot.*;

public class Enemy extends Actor
{
    private int hp;
    private int maxHp;
    private int x;
    private int y;
    
    public int getHp(){
        return hp;
    }
    
    public int getMaxHp(){
        return maxHp;
    }
    
    public int x(){
        return x;
    }
    
    public int y(){
        return y;
    }
}